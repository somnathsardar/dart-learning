void main()
{
  var name = "Somnath Sardar";

  // if-else statement.
  if(name == 'Somnath')
  {
    print("Matched");
  }
  else
  {
    print("Not matched");
  }

  //else if
  if(name == 'Somnath')
  {
    print("Found in if block");
  }
  else if(name == 'Somnath Sardar')
  {
    print("Found in else if block");
  }
  else
  {
    print("Not found");
  }
}